package advance.dev;

public class Student {
	String name;
	int age;
	String address;
	int numberPhone;
	float diemTB;
	public Student(String name, int age, String address, int numberPhone, float diemTB) {
		super();
		this.name = name;
		this.age = age;
		this.address = address;
		this.numberPhone = numberPhone;
		this.diemTB = diemTB;
	}

}
